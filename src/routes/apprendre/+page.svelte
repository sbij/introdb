<h1>Apprendre</h1>

Deux parties : apprentissage du fonctionnement du site, mais aussi apprentissage plus général des théories de la connaissance, de l'argumentation, du raisonnement.


<h2>Fonctionnement du site</h2>

On divise en deux éléments séparés : les propositions, les arguments. On peut travailler sur les deux en partant des deux bouts : les propositions avec leurs justifications, mais aussi les arguments avec leur logique.





